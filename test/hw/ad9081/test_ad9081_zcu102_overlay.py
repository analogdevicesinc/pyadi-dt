"""AD9081 + ZCU102 runtime overlay load/unload test.

LG_ENV: /jenkins/lg_ad9081_zcu102.yaml

Tests that a device tree overlay generated by the XSA pipeline can be:
1. Compiled from .dtso to .dtbo with external symbol preservation
2. Deployed to a running ZCU102 booted from Kuiper SD card
3. Loaded at runtime via configfs (/sys/kernel/config/device-tree/overlays/)
4. Verified by checking that AD9081 IIO devices appear
5. Unloaded by removing the configfs overlay directory
6. Verified that AD9081 IIO devices disappear
7. Reloaded to confirm repeatability

Prerequisites:
    - LG_ENV=/jenkins/lg_ad9081_zcu102.yaml
    - sdtgen and dtc on PATH
    - ZCU102 running Kuiper Linux with CONFIG_OF_OVERLAY=y
    - Board booted with a base DTB (Kuiper default) without AD9081 overlay

Flow:
    XSA → XsaPipeline → .dtso → dtc -@ → .dtbo → scp to target → configfs load → IIO check
"""

from __future__ import annotations

import os
import shutil
from pathlib import Path

import pytest

from adidt.xsa.pipeline import XsaPipeline
from test.hw.hw_helpers import compile_dtso_to_dtbo, shell_out

PROFILE_NAME = "ad9081_zcu102"
LG_ENV_PATH = "/jenkins/lg_ad9081_zcu102.yaml"
XSA_PATH = Path(__file__).parent / "system_top.xsa"

# JESD params for this XSA design (M8/L4 mode)
JESD_CFG = {
    "jesd": {
        "rx": {"F": 4, "K": 32, "M": 8, "L": 4, "Np": 16, "S": 1},
        "tx": {"F": 4, "K": 32, "M": 8, "L": 4, "Np": 16, "S": 1},
    },
}
OVERLAY_NAME = "ad9081_zcu102_xsa"
CONFIGFS_OVERLAYS = "/sys/kernel/config/device-tree/overlays"
DTBO_REMOTE_PATH = f"/tmp/{OVERLAY_NAME}.dtbo"

HERE = Path(__file__).parent
OUT_DIR = HERE / "output_overlay"

# IIO device names expected after overlay load
AD9081_IIO_NAMES = [
    "hmc7044",
    "axi-ad9081-rx-hpc",
    "axi-ad9081-tx-hpc",
    "ad9081",
]

if not os.environ.get("LG_ENV"):
    pytest.skip(
        f"set LG_ENV={LG_ENV_PATH} to run this test",
        allow_module_level=True,
    )


def _require_tools() -> None:
    if shutil.which("sdtgen") is None:
        pytest.skip("sdtgen not found on PATH")
    if shutil.which("dtc") is None:
        pytest.skip("dtc not found on PATH")


@pytest.fixture(scope="module")
def booted_board(board):
    """Boot the board to a shell via KuiperDLDriver + BootFPGASoC strategy."""
    kuiper = board.target.get_driver("KuiperDLDriver")
    kuiper.get_boot_files_from_release()
    board.transition("shell")
    return board


@pytest.fixture(scope="module")
def overlay_dtbo(tmp_path_factory) -> Path:
    """Generate and compile the AD9081 ZCU102 overlay .dtbo."""
    _require_tools()

    if not XSA_PATH.exists():
        pytest.skip(f"XSA not found: {XSA_PATH}")

    out_dir = tmp_path_factory.mktemp("overlay") / "out"
    result = XsaPipeline().run(
        xsa_path=XSA_PATH,
        cfg=JESD_CFG,
        output_dir=out_dir,
        profile=PROFILE_NAME,
        sdtgen_timeout=300,
    )

    overlay = result["overlay"]
    assert overlay.exists(), f"overlay not generated: {overlay}"

    dtbo = out_dir / f"{OVERLAY_NAME}.dtbo"
    compile_dtso_to_dtbo(overlay, dtbo)
    assert dtbo.exists() and dtbo.stat().st_size > 100
    print(f"Compiled overlay: {dtbo} ({dtbo.stat().st_size} bytes)")
    return dtbo


def _get_iio_names(shell) -> list[str]:
    """Read all IIO device names from the target."""
    raw = shell_out(shell, "cat /sys/bus/iio/devices/*/name 2>/dev/null; true")
    return [n.strip() for n in raw.splitlines() if n.strip()]


def _overlay_is_loaded(shell) -> bool:
    """Check if the overlay directory exists in configfs."""
    result = shell_out(
        shell, f"test -d {CONFIGFS_OVERLAYS}/{OVERLAY_NAME} && echo YES || echo NO"
    )
    return "YES" in result


def _load_overlay(shell) -> str:
    """Load the overlay via configfs. Returns the shell output."""
    shell_out(shell, f"mkdir -p {CONFIGFS_OVERLAYS}/{OVERLAY_NAME}")
    return shell_out(
        shell,
        f"echo -n {DTBO_REMOTE_PATH} > {CONFIGFS_OVERLAYS}/{OVERLAY_NAME}/path 2>&1; echo RC=$?",
    )


def _unload_overlay(shell) -> str:
    """Unload the overlay by removing the configfs directory."""
    return shell_out(
        shell,
        f"rmdir {CONFIGFS_OVERLAYS}/{OVERLAY_NAME} 2>&1; echo RC=$?",
    )


