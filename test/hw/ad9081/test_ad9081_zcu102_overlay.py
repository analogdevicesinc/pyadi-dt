@pytest.mark.lg_feature(["ad9081", "zcu102"])
def test_configfs_overlay_support(booted_board):
    """Verify the target kernel has configfs overlay support."""
    shell = booted_board.target.get_driver("ADIShellDriver")
    result = shell_out(shell, f"test -d {CONFIGFS_OVERLAYS} && echo OK || echo MISSING")
    assert "OK" in result, (
        f"configfs overlay directory not found at {CONFIGFS_OVERLAYS}. "
        "Ensure CONFIG_OF_OVERLAY=y in kernel config."
    )


@pytest.mark.lg_feature(["ad9081", "zcu102"])
def _deploy_dtbo_via_shell(shell, dtbo_path: Path, remote_path: str) -> None:
    """Transfer a .dtbo file to the target via base64 over the serial shell.

    This avoids requiring SSH/network connectivity — only serial console
    access is needed.
    """
    import base64

    data = dtbo_path.read_bytes()
    b64 = base64.b64encode(data).decode("ascii")

    # Write in chunks to avoid serial line length limits
    chunk_size = 512
    shell_out(shell, f"rm -f {remote_path}")
    for i in range(0, len(b64), chunk_size):
        chunk = b64[i : i + chunk_size]
        shell_out(shell, f"echo -n '{chunk}' >> {remote_path}.b64")
    shell_out(shell, f"base64 -d {remote_path}.b64 > {remote_path}")
    shell_out(shell, f"rm -f {remote_path}.b64")

    # Verify transfer
    remote_size = shell_out(shell, f"stat -c %s {remote_path} 2>/dev/null; true").strip()
    assert remote_size == str(len(data)), (
        f"dtbo transfer size mismatch: local={len(data)}, remote={remote_size}"
    )


@pytest.mark.lg_feature(["ad9081", "zcu102"])
def test_load_overlay(booted_board, overlay_dtbo):
    """Load the AD9081 overlay and verify IIO devices appear."""
    shell = booted_board.target.get_driver("ADIShellDriver")

    # Deploy .dtbo to target via serial (no SSH required)
    _deploy_dtbo_via_shell(shell, overlay_dtbo, DTBO_REMOTE_PATH)

    # Clean any previous load
    if _overlay_is_loaded(shell):
        _unload_overlay(shell)

    # Load overlay
    result = _load_overlay(shell)
    assert "RC=0" in result, f"overlay load failed: {result}"

    # Wait for driver probe
    import time
    time.sleep(5)

    # Check dmesg for errors
    dmesg = shell_out(
        shell, "dmesg | tail -30 | grep -i 'error\\|panic\\|fault'; true"
    )
    if dmesg.strip():
        print(f"dmesg errors after load:\n{dmesg}")

    # Verify IIO devices
    names = _get_iio_names(shell)
    print(f"IIO devices after load: {names}")

    found = any(n in names for n in AD9081_IIO_NAMES)
    assert found, (
        f"No AD9081 IIO device found after overlay load. "
        f"Expected one of {AD9081_IIO_NAMES}; found: {names}"
    )


@pytest.mark.lg_feature(["ad9081", "zcu102"])
def test_unload_overlay(booted_board):
    """Unload the AD9081 overlay via configfs rmdir without kernel errors."""
    shell = booted_board.target.get_driver("ADIShellDriver")

    if not _overlay_is_loaded(shell):
        pytest.skip("overlay not loaded — run test_load_overlay first")

    names_before = _get_iio_names(shell)
    print(f"IIO devices before unload: {names_before}")

    result = _unload_overlay(shell)
    assert "RC=0" in result, f"overlay unload failed: {result}"

    import time
    time.sleep(3)

    # Check for kernel errors during unload (exclude benign firmware load
    # messages — configfs overlay path triggers a firmware-style lookup first)
    dmesg = shell_out(
        shell,
        "dmesg | tail -10 | grep -i 'panic\\|oops\\|BUG:' ; true",
    )
    assert not dmesg.strip(), f"kernel errors during overlay unload:\n{dmesg}"

    # Verify the configfs directory was removed
    assert not _overlay_is_loaded(shell), "overlay configfs entry still present"

    names_after = _get_iio_names(shell)
    print(f"IIO devices after unload: {names_after}")

    # Note: if the base Kuiper DTB already includes AD9081 nodes, the IIO
    # devices will persist after overlay unload because they came from the
    # base DTB, not from the overlay.  The important check is that the
    # unload itself succeeds without kernel errors.


@pytest.mark.lg_feature(["ad9081", "zcu102"])
def test_reload_overlay(booted_board, overlay_dtbo):
    """Reload the overlay after unload to verify repeatability."""
    shell = booted_board.target.get_driver("ADIShellDriver")

    # Ensure clean state
    if _overlay_is_loaded(shell):
        _unload_overlay(shell)

    # Reload
    result = _load_overlay(shell)
    assert "RC=0" in result, f"overlay reload failed: {result}"

    import time
    time.sleep(5)

    names = _get_iio_names(shell)
    print(f"IIO devices after reload: {names}")

    found = any(n in names for n in AD9081_IIO_NAMES)
    assert found, (
        f"No AD9081 IIO device found after overlay reload. "
        f"Expected one of {AD9081_IIO_NAMES}; found: {names}"
    )

    # Clean up
    _unload_overlay(shell)


