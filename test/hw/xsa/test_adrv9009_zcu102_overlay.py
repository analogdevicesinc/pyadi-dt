@pytest.mark.lg_feature(["adrv9009", "zcu102"])
class TestOverlayLoadUnload:
    """Runtime overlay load/unload on a live ZCU102 target.

    Requires the board to be booted with a base Kuiper DTB that does NOT
    include ADRV9009 nodes.  The overlay adds them at runtime via configfs.
    """

    @pytest.fixture(autouse=True)
    def _require_hardware(self):
        _require_env()
        _require_tools()

    @pytest.fixture(scope="class")
    def overlay_dtbo(self, tmp_path_factory):
        """Generate and compile the ADRV9009 overlay .dtbo."""
        from test.xsa.kuiper_release import download_project_xsa

        tmp = tmp_path_factory.mktemp("overlay")
        xsa_path = download_project_xsa(
            release=os.environ.get("ADI_KUIPER_BOOT_RELEASE", "2023_r2"),
            project_dir="zynqmp-zcu102-rev10-adrv9009",
            cache_dir=tmp / "cache",
            output_dir=tmp / "xsa",
        )
        if not xsa_path.exists():
            pytest.skip("could not download ADRV9009 ZCU102 XSA")

        out_dir = tmp / "out"
        result = XsaPipeline().run(
            xsa_path=xsa_path,
            cfg={},
            output_dir=out_dir,
            profile=PROFILE_NAME,
            sdtgen_timeout=300,
        )

        dtbo = out_dir / f"{OVERLAY_NAME}.dtbo"
        compile_dtso_to_dtbo(result["overlay"], dtbo)
        return dtbo

    def test_configfs_overlay_support(self, board):
        """Verify the target kernel has configfs overlay support."""
        shell = board.target.get_driver("ADIShellDriver")
        result = shell_out(
            shell, f"test -d {CONFIGFS_OVERLAYS} && echo OK || echo MISSING"
        )
        assert "OK" in result, (
            f"configfs overlay directory not found at {CONFIGFS_OVERLAYS}. "
            "Ensure CONFIG_OF_OVERLAY=y in the kernel config."
        )

    def test_load_overlay(self, board, overlay_dtbo):
        """Load the ADRV9009 overlay via configfs and verify IIO devices appear."""
        shell = board.target.get_driver("ADIShellDriver")

        # Deploy .dtbo to target
        # (Assumes SSH or file transfer is available via the board driver)
        ssh = board.target.get_driver("SSHDriver")
        ssh.put(str(overlay_dtbo), DTBO_DEPLOY_PATH)

        # Ensure overlay is not already loaded
        shell_out(shell, f"rmdir {CONFIGFS_OVERLAYS}/{OVERLAY_NAME} 2>/dev/null; true")

        # Load overlay
        shell_out(shell, f"mkdir -p {CONFIGFS_OVERLAYS}/{OVERLAY_NAME}")
        result = shell_out(
            shell,
            f"echo -n {DTBO_DEPLOY_PATH} > {CONFIGFS_OVERLAYS}/{OVERLAY_NAME}/path 2>&1; echo RC=$?",
        )
        assert "RC=0" in result, f"overlay load failed: {result}"

        # Wait for driver probe
        import time

        time.sleep(5)

        # Verify IIO devices appeared
        iio_names = shell_out(
            shell, "cat /sys/bus/iio/devices/*/name 2>/dev/null; true"
        )
        print(f"IIO devices after overlay load: {iio_names}")

        adrv9009_found = any(
            name in iio_names
            for name in ["adrv9009-phy", "adrv9025-phy", "axi-adrv9009"]
        )
        assert adrv9009_found, (
            f"No ADRV9009 IIO device found after overlay load. "
            f"IIO devices present: {iio_names}"
        )

    def test_unload_overlay(self, board):
        """Unload the ADRV9009 overlay and verify IIO devices disappear."""
        shell = board.target.get_driver("ADIShellDriver")

        # Capture IIO devices before unload
        iio_before = shell_out(
            shell, "cat /sys/bus/iio/devices/*/name 2>/dev/null; true"
        )
        print(f"IIO devices before overlay unload: {iio_before}")

        # Unload overlay
        result = shell_out(
            shell,
            f"rmdir {CONFIGFS_OVERLAYS}/{OVERLAY_NAME} 2>&1; echo RC=$?",
        )
        assert "RC=0" in result, f"overlay unload failed: {result}"

        # Wait for driver removal
        import time

        time.sleep(3)

        # Verify ADRV9009 IIO devices are gone
        iio_after = shell_out(
            shell, "cat /sys/bus/iio/devices/*/name 2>/dev/null; true"
        )
        print(f"IIO devices after overlay unload: {iio_after}")

        adrv9009_still = any(
            name in iio_after
            for name in ["adrv9009-phy", "adrv9025-phy", "axi-adrv9009"]
        )
        assert not adrv9009_still, (
            f"ADRV9009 IIO device still present after overlay unload. "
            f"IIO devices: {iio_after}"
        )

    def test_reload_overlay(self, board, overlay_dtbo):
        """Reload the overlay after unload to verify repeatability."""
        shell = board.target.get_driver("ADIShellDriver")

        # Ensure clean state
        shell_out(shell, f"rmdir {CONFIGFS_OVERLAYS}/{OVERLAY_NAME} 2>/dev/null; true")

        # Reload
        shell_out(shell, f"mkdir -p {CONFIGFS_OVERLAYS}/{OVERLAY_NAME}")
        result = shell_out(
            shell,
            f"echo -n {DTBO_DEPLOY_PATH} > {CONFIGFS_OVERLAYS}/{OVERLAY_NAME}/path 2>&1; echo RC=$?",
        )
        assert "RC=0" in result, f"overlay reload failed: {result}"

        import time

        time.sleep(5)

        iio_names = shell_out(
            shell, "cat /sys/bus/iio/devices/*/name 2>/dev/null; true"
        )
        print(f"IIO devices after overlay reload: {iio_names}")

        adrv9009_found = any(
            name in iio_names
            for name in ["adrv9009-phy", "adrv9025-phy", "axi-adrv9009"]
        )
        assert adrv9009_found, (
            f"No ADRV9009 IIO device found after overlay reload. "
            f"IIO devices present: {iio_names}"
        )

        # Clean up
        shell_out(shell, f"rmdir {CONFIGFS_OVERLAYS}/{OVERLAY_NAME} 2>/dev/null; true")
