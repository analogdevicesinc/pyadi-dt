"""ADRV9009 + ZCU102 runtime overlay load/unload test.

Tests that a device tree overlay generated by the XSA pipeline can be:
1. Compiled from .dtso to .dtbo with external symbol preservation
2. Deployed to a running ZCU102 target
3. Loaded at runtime via configfs (/sys/kernel/config/device-tree/overlays/)
4. Verified by checking that IIO devices appear after loading
5. Unloaded by removing the configfs overlay directory
6. Verified that IIO devices disappear after unloading

Prerequisites:
    - LG_ENV pointing to an ADRV9009+ZCU102 labgrid environment
    - sdtgen and dtc on PATH
    - ZCU102 running Kuiper Linux with CONFIG_OF_OVERLAY=y
    - Board booted with a base DTB that does NOT include ADRV9009 nodes

Flow:
    XSA → XsaPipeline → .dtso → dtc -@ → .dtbo → deploy → load → verify → unload → verify
"""

from __future__ import annotations

import os
import shutil
from pathlib import Path

import pytest

from adidt.xsa.pipeline import XsaPipeline
from test.hw.hw_helpers import compile_dtso_to_dtbo, shell_out

PROFILE_NAME = "adrv9009_zcu102"
LG_ENV_PATH = os.environ.get("LG_ENV", "")
OVERLAY_NAME = "adrv9009_zcu102_xsa"
CONFIGFS_OVERLAYS = "/sys/kernel/config/device-tree/overlays"
DTBO_DEPLOY_PATH = f"/tmp/{OVERLAY_NAME}.dtbo"

HERE = Path(__file__).parent
OUT_DIR = HERE / "output_adrv9009_overlay"


def _require_env() -> None:
    if not LG_ENV_PATH:
        pytest.skip("set LG_ENV to run this hardware test")
    if not Path(LG_ENV_PATH).exists():
        pytest.skip(f"labgrid env file missing: {LG_ENV_PATH}")


def _require_tools() -> None:
    if shutil.which("sdtgen") is None:
        pytest.skip("sdtgen not found on PATH")
    if shutil.which("dtc") is None:
        pytest.skip("dtc not found on PATH")


# ---------------------------------------------------------------------------
# Unit tests (no hardware required)
# ---------------------------------------------------------------------------


class TestOverlayGeneration:
    """Test that the pipeline generates a valid .dtso and it compiles to .dtbo."""

    def test_pipeline_generates_dtso(self, tmp_path):
        """XsaPipeline produces a .dtso overlay file."""
        from test.xsa.kuiper_release import download_project_xsa

        xsa_path = download_project_xsa(
            release=os.environ.get("ADI_KUIPER_BOOT_RELEASE", "2023_r2"),
            project_dir="zynqmp-zcu102-rev10-adrv9009",
            cache_dir=tmp_path / "cache",
            output_dir=tmp_path / "xsa",
        )
        if not xsa_path.exists():
            pytest.skip("could not download ADRV9009 ZCU102 XSA")

        result = XsaPipeline().run(
            xsa_path=xsa_path,
            cfg={},
            output_dir=tmp_path / "out",
            profile=PROFILE_NAME,
            sdtgen_timeout=300,
        )
        overlay = result["overlay"]
        assert overlay.exists(), f"overlay not generated: {overlay}"

        text = overlay.read_text()
        assert "/plugin/;" in text, "overlay missing /plugin/ directive"
        assert "adi,adrv9009" in text or "adrv9009" in text, (
            "overlay missing ADRV9009 compatible"
        )

    @pytest.mark.skipif(shutil.which("dtc") is None, reason="dtc not found on PATH")
    def test_dtso_compiles_to_dtbo(self, tmp_path):
        """A generated .dtso compiles to .dtbo with dtc -@."""
        from test.xsa.kuiper_release import download_project_xsa

        xsa_path = download_project_xsa(
            release=os.environ.get("ADI_KUIPER_BOOT_RELEASE", "2023_r2"),
            project_dir="zynqmp-zcu102-rev10-adrv9009",
            cache_dir=tmp_path / "cache",
            output_dir=tmp_path / "xsa",
        )
        if not xsa_path.exists():
            pytest.skip("could not download ADRV9009 ZCU102 XSA")

        result = XsaPipeline().run(
            xsa_path=xsa_path,
            cfg={},
            output_dir=tmp_path / "out",
            profile=PROFILE_NAME,
            sdtgen_timeout=300,
        )

        dtbo = tmp_path / "out" / f"{OVERLAY_NAME}.dtbo"
        compile_dtso_to_dtbo(result["overlay"], dtbo)
        assert dtbo.exists(), f"dtbo not generated: {dtbo}"
        assert dtbo.stat().st_size > 100, "dtbo suspiciously small"


